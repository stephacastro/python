
def operacoes_matematicas_soma(n1, n2):
    print('************** Função de Soma **************')
    n1 = int(input('Digite n2: '))
    n2 = int(input('Digite n1: '))

    soma = n1 + n2
    print(f'O resultado entre a soma de {n1} + {n2}: {soma}')

